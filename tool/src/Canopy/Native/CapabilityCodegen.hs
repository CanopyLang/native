-- | One file the generator emits: a package-root-relative path + its contents. The CLI writes each
-- under @\<outDir\>/canopy/\<name\>/@ (or wherever the caller roots the package).
data GeneratedFile = GeneratedFile
  { gfPath    :: !FilePath  -- ^ package-relative path, e.g. "src/Vibration.can"
  , gfContent :: !Text
  } deriving (Eq, Show)

-- | The complete set of files for a self-contained capability package. This is the AUTO-E-DELETE
-- deliverable: @gen-capability Foo@ produces a package satisfying DoD #1 (canopy.json + src/Foo.can +
-- native/android + native/ios) with no manual file placement, so adding the capability to an app is
-- "add a dependency" — exactly like @canopy/http@ ships @external/http.js@.
--
-- The package-relative layout (rooted at @canopy/\<name\>/@):
--
--   * @canopy.json@                          — package metadata + deps
--   * @native.json@                          — the autolink manifest
--   * @src/\<Name\>.can@                      — the Canopy module (Task wrappers)
--   * @native/android/\<Name\>Module.java@    — the Android JniModule dispatcher
--   * @native/ios/Canopy\<Name\>Module.mm@    — the iOS <CanopyModule> twin
--   * @harness/mock.js@                       — a device-free harness mock
packageFiles :: CapabilitySpec -> [GeneratedFile]
packageFiles spec =
  [ GeneratedFile "canopy.json"                                        (renderCanopyJson spec       <> "\n")
  , GeneratedFile "native.json"                                        (renderPackageNativeJson spec <> "\n")
  , GeneratedFile ("src/" <> name <> ".can")                           (renderCanModule spec        <> "\n")
  , GeneratedFile ("native/android/" <> name <> "Module.java")         (renderJavaModule spec       <> "\n")
  , GeneratedFile ("native/ios/Canopy" <> name <> "Module.mm")         (renderIosModule spec        <> "\n")
  , GeneratedFile "harness/mock.js"                                    (renderMockEntry spec        <> "\n")
  ]
  where name = T.unpack (capName spec)
